package br.com.senai;

public class Carro {
	
	//ATRIBUTOS
	
	private String marca;
	private String modelo;
	private int ano;
	private int qndPorta;
	
	
	//METODOS
	
	public String getMarca() {
		return marca;
	}
	
	public void setMarca(String marca) {
		this.marca = marca;
	}
	
	
	
	public String getModelo() {
		return modelo;
	}
	
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	
	
	
	public int getAno() {
		return ano;
	}
	
	public void setAno(int ano) {
		this.ano = ano;
	}
	
	
	
	public int getQndPorta() {
		return qndPorta;
	}
	
	public void setQndPorta(int qndPorta) {
		this.qndPorta = qndPorta;
	}

	}
	
	

